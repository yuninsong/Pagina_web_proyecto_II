    <h1 class="text-center text-custom --about-title">Acerca de la UNEXCA</h2>
    <p class="text-center --about-text">La Universidad Nacional Experimental de Caracas (UNEXCA), en su sede de Altagracia, se consolida como un referente académico y cultural en el corazón de Venezuela. Como parte de una institución pública comprometida con la excelencia educativa, nuestra sede en Altagracia se distingue por ofrecer una formación integral en diversas áreas del conocimiento, adaptada a las necesidades de la comunidad y al desarrollo regional.
        <br>
        <br>
En UNEXCA Altagracia, nos enorgullece combinar tradición e innovación, brindando a nuestros estudiantes un entorno propicio para el aprendizaje, la investigación y el crecimiento personal. Nuestro equipo docente altamente calificado y nuestras instalaciones modernas garantizan una experiencia educativa de calidad, orientada a formar profesionales competentes y comprometidos con el progreso social.
<br>
<br>
Además, nuestra sede se integra activamente con la comunidad de Altagracia, promoviendo proyectos de impacto social, cultural y ambiental que contribuyen al desarrollo sostenible de la región. En UNEXCA Altagracia, no solo formamos profesionales, sino también ciudadanos conscientes y agentes de cambio.
<br>
<br>
Únete a nosotros y sé parte de una institución que transforma vidas y construye futuro.</p>
</section>

